def multiplicar(num1, num2):
    return num1 * num2


def adicao(num3, num4):
    return num3 + num4


def subtrair(num5, num6):
    return num5 - num6


def dividir(num7, num8):
    return num7 / num8


print('Multiplique 8 x 2')
print(multiplicar(8, 2))
print('ok')

print('Some 5 x 7')
